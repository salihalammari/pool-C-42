int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	reslt;

	i = 0;
	sign = 1;
	reslt = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		reslt = (reslt * 10) + (str[i] - '0');
		i++;
	}
	return (reslt * sign);
}
